#!/usr/bin/env node
// Render each sandbox backend's Dockerfile from runtime.mjs (the single source of truth).
// The Daytona *.mjs builders import the spec directly; the Dockerfiles (E2B ships one to
// `template create`; the future local Docker `docker build`s one) are GENERATED here so
// they can never silently drift from the spec.
//
//   node deploy/pi-runtime/render-dockerfile.mjs --write   # (re)write every backend Dockerfile
//   node deploy/pi-runtime/render-dockerfile.mjs --check   # verify committed == generated; exit 1 on drift
//
// Bump pi: edit piVersion in runtime.mjs, then --write.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { baseImage, piVersion, piPackage, aptPackages, backends } from './runtime.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '../..');

// backend key → { out: repo-relative path, note: per-backend context } — the ONLY divergence
// is the runtime user/home, which comes from `backends` in the spec, not from here.
const TARGETS = {
  e2b: {
    out: 'deploy/e2b/e2b.Dockerfile',
    note:
      "E2B's default sandbox user is `user` (home /home/user); egress is OPEN so a remote/HTTP MCP\n" +
      '# server is reachable. Built via `e2b template create -d e2b.Dockerfile` (see deploy/e2b/build.md).',
  },
  daytona: {
    out: 'deploy/daytona/Dockerfile',
    note:
      "Daytona's runtime user is `daytona` (uid 1001). NOTE: the ACTUAL Daytona build is the declarative\n" +
      '# `Image` in deploy/daytona/*.mjs (which import runtime.mjs); this Dockerfile is the human-readable\n' +
      '# parity copy, generated from the same spec.',
  },
  docker: {
    out: 'deploy/docker/Dockerfile',
    note:
      'Local Docker backend (future): `docker build` this image, `docker run` it per run. Same recipe as\n' +
      '# the cloud images — the whole point of the shared spec.',
  },
};

function render(key) {
  const b = backends[key];
  const { note } = TARGETS[key];
  const run = [
    'RUN apt-get update \\',
    ` && apt-get install -y --no-install-recommends ${aptPackages.join(' ')} \\`,
    ' && rm -rf /var/lib/apt/lists/* \\',
    ` && npm install -g --ignore-scripts "${piPackage}@\${PI_VERSION}" \\`,
    ' && pi --version',
  ].join('\n');
  const userBlock = b.user
    ? "\n# Match the platform's runtime user + home so the runner's in-VM paths resolve identically.\n" +
      `RUN id ${b.user.name} >/dev/null 2>&1 || useradd -m -u ${b.user.uid} ${b.user.name}\n`
    : '';
  return `# GENERATED by deploy/pi-runtime/render-dockerfile.mjs from runtime.mjs — DO NOT EDIT.
# Bump pi / packages / base in runtime.mjs, then: node deploy/pi-runtime/render-dockerfile.mjs --write
#
# piflow "pi node-runtime" image — bakes the RUNTIME (one \`pi\` process per node), NOT the
# per-node files (the host runner stages those in at run time). Full rationale: runtime.mjs.
# ${note}

FROM ${baseImage}

ARG PI_VERSION=${piVersion}

# One RUN layer so the apt cache never persists in a layer.
${run}
${userBlock}WORKDIR ${b.workdir}
`;
}

const mode = process.argv[2];
if (mode !== '--write' && mode !== '--check') {
  console.error('usage: render-dockerfile.mjs --write | --check');
  process.exit(2);
}

let drift = 0;
for (const key of Object.keys(TARGETS)) {
  const out = resolve(repoRoot, TARGETS[key].out);
  const next = render(key);
  if (mode === '--write') {
    mkdirSync(dirname(out), { recursive: true });
    writeFileSync(out, next);
    console.log(`wrote ${TARGETS[key].out}`);
  } else {
    let cur = '';
    try {
      cur = readFileSync(out, 'utf8');
    } catch {
      cur = '';
    }
    if (cur === next) {
      console.log(`ok   ${TARGETS[key].out}`);
    } else {
      drift++;
      console.error(`DRIFT ${TARGETS[key].out} — differs from runtime.mjs (run --write)`);
    }
  }
}
if (mode === '--check' && drift) process.exit(1);
